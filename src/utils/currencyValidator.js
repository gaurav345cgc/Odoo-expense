/**
 * Checks if a given string is a valid ISO 4217 currency code.
 * @param {string} code The currency code to validate.
 * @returns {boolean} True if the code is valid, false otherwise.
 */
const isValidCurrencyCode = (code) => {
  if (typeof code !== 'string') {
    return false;
  }
  return validCurrencyCodes.has(code.toUpperCase());
};

